
  create view `hotel_booking`.`stg_iceberg_raw_hotel_bookings__dbt_tmp` as 

SELECT
    source_dataset,
    CAST(original_source_row_number AS BIGINT) AS original_source_row_number,
    booking_key,
    batch_id,
    CAST(batch_sequence AS INT) AS batch_sequence,
    CAST(batch_effective_at AS DATETIME) AS batch_effective_at,
    CAST(batch_row_number AS BIGINT) AS batch_row_number,
    COALESCE(CAST(etl_year AS INT), YEAR(CAST(batch_effective_at AS DATETIME))) AS etl_year,
    COALESCE(CAST(etl_month AS INT), MONTH(CAST(batch_effective_at AS DATETIME))) AS etl_month,
    COALESCE(CAST(etl_day AS INT), DAYOFMONTH(CAST(batch_effective_at AS DATETIME))) AS etl_day,
    COALESCE(watermark_date, DATE_FORMAT(CAST(batch_effective_at AS DATETIME), '%Y%m%d')) AS watermark_date,
    COALESCE(raw_batch_sequence, LPAD(CAST(batch_sequence AS VARCHAR), 3, '0')) AS raw_batch_sequence,
    source_file_name,
    source_object_path,
    file_hash,
    record_hash,
    CAST(ingested_at AS DATETIME) AS ingested_at,
    row_ingestion_id,
    synthetic_operation,
    hotel,
    is_canceled,
    lead_time,
    arrival_date_year,
    arrival_date_month,
    arrival_date_week_number,
    arrival_date_day_of_month,
    stays_in_weekend_nights,
    stays_in_week_nights,
    adults,
    children,
    babies,
    meal,
    country,
    market_segment,
    distribution_channel,
    is_repeated_guest,
    previous_cancellations,
    previous_bookings_not_canceled,
    reserved_room_type,
    assigned_room_type,
    booking_changes,
    deposit_type,
    agent,
    company,
    days_in_waiting_list,
    customer_type,
    adr,
    required_car_parking_spaces,
    total_of_special_requests,
    reservation_status,
    reservation_status_date
FROM `iceberg_catalog`.`hotel_booking_lakehouse`.`raw_hotel_bookings_history`;