            LOWER(TRIM(COALESCE(CAST(distribution_channel AS VARCHAR), ''))),
            LOWER(TRIM(COALESCE(CAST(is_repeated_guest AS VARCHAR), ''))),
            LOWER(TRIM(COALESCE(CAST(previous_cancellations AS VARCHAR), ''))),
            LOWER(TRIM(COALESCE(CAST(previous_bookings_not_canceled AS VARCHAR), ''))),
