            LOWER(TRIM(COALESCE(CAST(assigned_room_type AS VARCHAR), ''))),
            LOWER(TRIM(COALESCE(CAST(booking_changes AS VARCHAR), ''))),
            LOWER(TRIM(COALESCE(CAST(deposit_type AS VARCHAR), ''))),
